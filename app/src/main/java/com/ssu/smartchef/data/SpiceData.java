package com.ssu.smartchef.data;

public class SpiceData {
    private int num;
    private String name;

    public SpiceData(){
       num = 0;
       name = "";
    }
    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


}
